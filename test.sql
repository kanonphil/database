CREATE TABLE ORDER_ITEM (
	ORDER_ITEM_ID INT PRIMARY KEY AUTO_INCREMENT,
	ORDER_ID INT NOT NULL,
	PRODUCT_ID INT NOT NULL,
	ORDER_ITEM_QTY INT NOT NULL,
	ORDER_ITEM_PRICE DECIMAL(10, 2) NOT NULL,
	FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ORDER_ID),
	FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT (PRODUCT_ID)
);

CREATE TABLE DELIVERY (
	DELIVERY_ID INT PRIMARY KEY AUTO_INCREMENT,
	ORDER_ID INT NOT NULL,
	RECIPIENT_NAME VARCHAR(50) NOT NULL,
	RECIPIIENT_PHONE VARCHAR(20) NOT NULL,
	RECIPIENT_ADDRESS VARCHAR(200) NOT NULL,
	RECIPIENT_ADDRESS_DETAIL VARCHAR(100),
	DELIVERY_STATUS VARCHAR(20) DEFAULT 'READY',  -- READY | SHIPPED | DELIVERED
	DELIVERY_CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP(),
	FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ORDER_ID)
);
