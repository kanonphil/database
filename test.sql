CREATE TABLE MEMBER (
	MEMBER_ID INT PRIMARY KEY AUTO_INCREMENT,
	MEMBER_EMAIL VARCHAR(100) NOT NULL UNIQUE,
	MEMBER_PASSWORD VARCHAR(255) NOT NULL,
	MEMBER_NAME VARCHAR(50) NOT NULL,
	MEMBER_PHONE VARCHAR(20) NOT NULL,
	MEMBER_BIRTH DATE NOT NULL,
	MEMBER_ADDR VARCHAR(255) NOT NULL,
	MEMBER_ADDR_DETAIL VARCHAR(255),
	MEMBER_ROLE VARCHAR(20) NOT NULL DEFAULT 'ROLE_USER',  # ROLE_USER | ROLE_MANAGER
	MEMBER_STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',  # ACTIVE | INACTIVE
	MEMBER_CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

SELECT * FROM MEMBER;

CREATE TABLE CATEGORY (
	CATEGORY_ID INT PRIMARY KEY AUTO_INCREMENT,
	CATEGORY_NAME VARCHAR(50) NOT NULL
);

SELECT * FROM CATEGORY;

CREATE TABLE PRODUCT (
	PRODUCT_ID INT PRIMARY KEY AUTO_INCREMENT,
	CATEGORY_ID INT NOT NULL,
	PRODUCT_NAME VARCHAR(100) NOT NULL,
	PRODUCT_PRICE INT NOT NULL,
	PRODUCT_STOCK INT NOT NULL,
	PRODUCT_DESC TEXT,
	PRODUCT_STATUS VARCHAR(20) DEFAULT 'ACTIVE',  # ACTIVE | INACTIVE
	PRODUCT_CREATED_AT DATETIME DEFAULT NOW(),
	FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY (CATEGORY_ID)
);

ALTER TABLE PRODUCT MODIFY COLUMN PRODUCT_STOCK INT NOT NULL DEFAULT 0;
ALTER TABLE PRODUCT MODIFY COLUMN PRODUCT_PRICE DECIMAL(10, 2) NOT NULL;

SELECT * FROM PRODUCT;

CREATE TABLE PRODUCT_IMAGE (
	IMAGE_ID INT PRIMARY KEY AUTO_INCREMENT,
	PRODUCT_ID INT NOT NULL,
	IMAGE_ORIGIN_NAME VARCHAR(255) NOT NULL,
	IMAGE_SAVED_NAME VARCHAR(255) NOT NULL,
	IMAGE_TYPE VARCHAR(10) NOT NULL,  # MAIN | DESC
	IMAGE_ORDER INT NOT NULL DEFAULT 1,  # MAIN 1번 = 썸네일
	FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT (PRODUCT_ID)
);

SELECT * FROM PRODUCT_IMAGE;

CREATE TABLE ORDERS (
	ORDER_ID INT PRIMARY KEY AUTO_INCREMENT,
	MEMBER_ID INT NOT NULL,
	ORDER_STATUS VARCHAR(20) NOT NULL DEFAULT 'READY',  # READY | PAID | SHIPPED | DONE | CANCEL
	ORDER_TOTAL_PRICE DECIMAL(10, 2) NOT NULL,
	ORDER_CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP(),
	FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER (MEMBER_ID)
);

